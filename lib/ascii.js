var ascii = require('glush-util').ascii;

function hence(placeholder, log) {
  /*
   '  _   _ _____ _   _  ____ _____   _              \n' +
   ' | | | | ____| \ | |/ ___| ____| (_) ___        \n' +
   ' | |_| |  _| |  \| | |   |  _|   | |/ _ \      \n' +
   ' |  _  | |___| |\  | |___| |___ _| | (_) |      \n' +
   ' |_| |_|_____|_| \_|\____|_____(_)_|\___/     \n',
   */

  return ascii.renderAsciiArt(
    '  _   _ _____ _   _  ____ _____   _              \n' +
    ' | | | | ____| \\ | |/ ___| ____| (_) ___        \n' +
    ' | |_| |  _| |  \\| | |   |  _|   | |/ _ \\      \n' +
    ' |  _  | |___| |\\  | |___| |___ _| | (_) |      \n' +
    ' |_| |_|_____|_| \\_|\\____|_____(_)_|\\___/     \n',
    placeholder, log);
}

module.exports = {
  hence: hence
};
